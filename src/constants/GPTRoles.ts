/**
 * Enumeration representing roles for GPT entities.
 */
export enum GPTRoles {
  /**
   * Role representing a user entity.
   */
  USER = <any>'user',
  /**
   * Role representing an assistant entity.
   */
  ASSISTANT = <any>'assistant',
  /**
   * Role representing a system entity.
   */
  SYSTEM = <any>'system',
}
